package org.apache.fineract.portfolio.fund.handler;

import org.apache.fineract.commands.annotation.CommandType;
import org.apache.fineract.commands.handler.NewCommandSourceHandler;
import org.apache.fineract.infrastructure.core.api.JsonCommand;
import org.apache.fineract.infrastructure.core.data.CommandProcessingResult;
import org.apache.fineract.portfolio.fund.service.FundWritePlatformService;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;
import org.springframework.transaction.annotation.Transactional;

@Service
@CommandType(entity = "FUND", action = "ACTIVATE")
public class ActivateFundCommandHandler implements NewCommandSourceHandler {

    private final FundWritePlatformService writePlatformService;

    @Autowired
    public ActivateFundCommandHandler(final FundWritePlatformService writePlatformService) {
        this.writePlatformService = writePlatformService;
    }

    @Transactional
    @Override
    public CommandProcessingResult processCommand(JsonCommand command) {
        // TODO Auto-generated method stub
        return this.writePlatformService.activateFund(command.entityId());
    }

}
